// JobBatchResponse Per-batch outcome.
type JobBatchResponse struct {
	// Accepted Number of deltas successfully applied.
	Accepted int `json:"accepted"`

	// Rejected Per-job rejection records.
	Rejected []struct {
		// Id Job identifier of the rejected delta.
		Id string `json:"id"`

		// Reason Reason the delta was rejected. One of `not_owner`,
		// `claim_superseded`, `already_terminal`, `invalid_status`,
		// `not_found`.
		//
		// Fencing: an epoch-BEARING delta (one carrying `claim_epoch`)
		// whose `(owner_instance_id, claim_epoch)` no longer matches the
		// live row — a different owner, a reclaimed `owner=null` row, or
		// the same owner at a newer epoch — is rejected
		// `claim_superseded` (the sender once held a claim it has since
		// lost). `not_owner` is reserved for epoch-LESS deltas (legacy
		// SDKs) whose owner does not match.
		Reason string `json:"reason"`
	} `json:"rejected"`
}

// JobDelta A single delta to apply to one job. Only fields present are updated.
// `result` is meaningful only when `status=completed`; `error` only when
// `status=failed`.
type JobDelta struct {
	// ClaimEpoch Claim generation this delta was produced under (from the claim
	// response). When present, the registry rejects the delta as
	// `claim_superseded` unless it matches the job's current
	// `claim_epoch` AND `owner_instance_id` — fencing a superseded owner
	// even on a same-instance re-claim. Omit (old SDKs) to fall back to
	// owner-only validation (`not_owner` on owner mismatch).
	ClaimEpoch *int64 `json:"claim_epoch"`

	// Error Failure reason (only on `status=failed`).
	Error *string `json:"error"`

	// Id Job identifier this delta targets.
	Id string `json:"id"`

	// Progress Updated fractional progress.
	Progress *float32 `json:"progress"`

	// ProgressMessage Updated human-readable progress note.
	ProgressMessage *string `json:"progress_message"`

	// RecvCursor Durable per-filter recvEvent cursor snapshot (issue #1277). Maps a
	// canonical event-filter key to the highest event `seq` the handler
	// has PROVABLY finished processing for that filter (a lagging cursor —
	// it trails receipt so at-least-once is preserved on resume). The
	// registry persists it verbatim onto the job row inside the SAME
	// epoch-fenced guarded UPDATE as progress, so a superseded delta never
	// advances it. Present only when the producing SDK tracks a durable
	// cursor; omit otherwise (the column is left untouched).
	RecvCursor *map[string]int64 `json:"recv_cursor"`

	// Result Terminal result payload (only on `status=completed`).
	Result *map[string]interface{} `json:"result"`

	// Status New status, if transitioning.
	Status *JobStatus `json:"status"`
}

