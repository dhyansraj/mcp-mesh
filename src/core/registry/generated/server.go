// ReleaseJobResponse Result of a voluntary release. `status=working` is the typical
// outcome — the job was released and is ready to be re-claimed by a
// peer replica. `status=failed` only fires in the boundary case
// where the row's `attempt_count` was already greater than
// `max_retries` when release was called (i.e. the final allowed
// claim's handler raised), so no further retries are possible and
// the registry marks the job exhausted.
type ReleaseJobResponse struct {
	// AttemptCount Current attempt count. Note: release does NOT increment this
	// field — only the claim path does. The value returned here is
	// the same value that was set by the claim that produced the
	// now-released owner.
	AttemptCount int `json:"attempt_count"`

	// Status Job lifecycle state. Terminal states are `completed`, `failed`, and
	// `cancelled`. `input_required` is a non-terminal pause state.
	Status JobStatus `json:"status"`
}

