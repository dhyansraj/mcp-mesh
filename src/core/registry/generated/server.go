// ServerInterface represents all server handlers.
type ServerInterface interface {
	// Registry root information
	// (GET /)
	GetRoot(c *gin.Context)
	// List A2A-exposed agents and their public URLs
	// (GET /a2a/agents)
	ListA2AAgents(c *gin.Context)
	// List all registered agents
	// (GET /agents)
	ListAgents(c *gin.Context)
	// Graceful agent unregistration
	// (DELETE /agents/{agent_id})
	UnregisterAgent(c *gin.Context, agentId string)
	// List recent registry events with filters
	// (GET /events)
	ListEvents(c *gin.Context, params ListEventsParams)
	// Registry health check
	// (GET /health)
	GetHealth(c *gin.Context)
	// Registry health check (headers only)
	// (HEAD /health)
	HeadHealth(c *gin.Context)
	// Agent registration and heartbeat
	// (POST /heartbeat)
	SendHeartbeat(c *gin.Context)
	// Fast agent health check
	// (HEAD /heartbeat/{agent_id})
	FastHeartbeatCheck(c *gin.Context, agentId string)
	// List jobs (paginated, filterable)
	// (GET /jobs)
	ListJobs(c *gin.Context, params ListJobsParams)
	// Register a new job
	// (POST /jobs)
	CreateJob(c *gin.Context)
	// Submit a batch of job deltas
	// (POST /jobs/batch)
	SubmitJobBatch(c *gin.Context)
	// Atomically claim an unclaimed job
	// (POST /jobs/claim)
	ClaimJobs(c *gin.Context)
	// Read latest job state
	// (GET /jobs/{job_id})
	GetJob(c *gin.Context, jobId string)
	// Cancel an in-flight job
	// (POST /jobs/{job_id}/cancel)
	CancelJob(c *gin.Context, jobId string)
	// Producer voluntarily releases lease (for transient-error retry)
	// (POST /jobs/{job_id}/release)
	ReleaseJob(c *gin.Context, jobId string)
	// List registered canonical schemas
	// (GET /schemas)
	ListSchemas(c *gin.Context, params ListSchemasParams)
	// Get a single canonical schema by content hash
	// (GET /schemas/{hash})
	GetSchema(c *gin.Context, hash string)
}

