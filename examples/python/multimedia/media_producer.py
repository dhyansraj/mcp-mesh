#!/usr/bin/env python3
"""
media-producer - MCP Mesh Media Producer Agent

Demonstrates generating media content and returning resource_links via the
mesh media API. Creates markdown reports, SVG charts, and PNG images using
only built-in Python — no external imaging libraries required.
"""

import mesh
from fastmcp import FastMCP
from mcp.types import ResourceLink

app = FastMCP("Media Producer")


@app.tool()
@mesh.tool(
    capability="report_generator", description="Generates a markdown report on a topic"
)
async def generate_report(topic: str = "AI") -> ResourceLink:
    """Generate a markdown report on a given topic."""
    slug = topic.lower().replace(" ", "_")
    content = f"""# Report: {topic}

## Executive Summary

This is an auto-generated report on **{topic}** produced by the media-producer agent.

## Key Findings

1. {topic} continues to evolve rapidly across multiple domains.
2. Adoption rates have increased significantly in the past year.
3. Integration with existing systems remains a priority.

## Recommendations

- Invest in foundational research for {topic}.
- Develop clear governance frameworks.
- Monitor emerging trends and adjust strategy accordingly.

---
*Generated by media-producer agent*
"""
    uri = await mesh.upload_media(
        data=content.encode("utf-8"),
        filename=f"report_{slug}.md",
        mime_type="text/markdown",
    )
    return mesh.media_result(
        uri=uri,
        name=f"Report: {topic}",
        mime_type="text/markdown",
        description=f"Markdown report on {topic}",
        size=len(content.encode("utf-8")),
    )


@app.tool()
@mesh.tool(
    capability="chart_generator", description="Generates an SVG bar chart from data"
)
async def generate_chart(
    data: str = "Sales:40,Marketing:25,Engineering:60,Support:15",
) -> ResourceLink:
    """Generate a simple SVG bar chart. Provide data as 'Label:Value,...' pairs."""
    pairs = []
    for item in data.split(","):
        parts = item.strip().split(":")
        if len(parts) == 2:
            try:
                pairs.append((parts[0].strip(), float(parts[1].strip())))
            except ValueError:
                continue

    if not pairs:
        pairs = [("A", 30), ("B", 50), ("C", 20)]

    max_val = max(v for _, v in pairs)
    bar_width = 60
    gap = 20
    chart_height = 200
    margin_bottom = 40
    margin_top = 20
    margin_left = 10
    total_width = margin_left + len(pairs) * (bar_width + gap) + gap
    total_height = chart_height + margin_bottom + margin_top

    colors = [
        "#4e79a7",
        "#f28e2b",
        "#e15759",
        "#76b7b2",
        "#59a14f",
        "#edc948",
        "#b07aa1",
        "#ff9da7",
    ]

    bars_svg = ""
    for i, (label, value) in enumerate(pairs):
        bar_height = (value / max_val) * chart_height if max_val > 0 else 0
        x = margin_left + gap + i * (bar_width + gap)
        y = margin_top + chart_height - bar_height
        color = colors[i % len(colors)]
        bars_svg += f'  <rect x="{x}" y="{y}" width="{bar_width}" height="{bar_height}" fill="{color}" rx="4"/>\n'
        bars_svg += f'  <text x="{x + bar_width / 2}" y="{margin_top + chart_height + 16}" text-anchor="middle" font-size="12" font-family="sans-serif" fill="#333">{label}</text>\n'
        bars_svg += f'  <text x="{x + bar_width / 2}" y="{y - 5}" text-anchor="middle" font-size="11" font-family="sans-serif" fill="#555">{value:g}</text>\n'

    svg = f"""<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 {total_width} {total_height}" width="{total_width}" height="{total_height}">
  <rect width="100%" height="100%" fill="#fafafa" rx="8"/>
  <line x1="{margin_left + gap}" y1="{margin_top + chart_height}" x2="{total_width - gap}" y2="{margin_top + chart_height}" stroke="#ccc" stroke-width="1"/>
{bars_svg}</svg>
"""
    svg_bytes = svg.encode("utf-8")
    uri = await mesh.upload_media(
        data=svg_bytes,
        filename="chart.svg",
        mime_type="image/svg+xml",
    )
    return mesh.media_result(
        uri=uri,
        name="Bar Chart",
        mime_type="image/svg+xml",
        description=f"SVG bar chart with {len(pairs)} data points",
        size=len(svg_bytes),
    )


@app.tool()
@mesh.tool(capability="image_generator", description="Returns a sample PNG image")
async def get_sample_image() -> ResourceLink:
    """Create and return a minimal valid PNG image (a small blue square)."""
    # Minimal 2x2 PNG with blue pixels, built from raw bytes.
    # PNG structure: signature + IHDR + IDAT + IEND
    import struct
    import zlib

    width, height = 2, 2
    # Each row: filter byte (0 = None) + RGB pixels
    raw_rows = b""
    for _ in range(height):
        raw_rows += b"\x00"  # filter byte
        for _ in range(width):
            raw_rows += b"\x33\x66\xcc"  # RGB blue-ish

    def _chunk(chunk_type: bytes, data: bytes) -> bytes:
        c = chunk_type + data
        crc = struct.pack(">I", zlib.crc32(c) & 0xFFFFFFFF)
        return struct.pack(">I", len(data)) + c + crc

    signature = b"\x89PNG\r\n\x1a\n"
    ihdr_data = struct.pack(">IIBBBBB", width, height, 8, 2, 0, 0, 0)  # 8-bit RGB
    ihdr = _chunk(b"IHDR", ihdr_data)
    idat = _chunk(b"IDAT", zlib.compress(raw_rows))
    iend = _chunk(b"IEND", b"")

    png_bytes = signature + ihdr + idat + iend

    uri = await mesh.upload_media(
        data=png_bytes,
        filename="sample_image.png",
        mime_type="image/png",
    )
    return mesh.media_result(
        uri=uri,
        name="Sample Image",
        mime_type="image/png",
        description="A small 2x2 blue PNG image",
        size=len(png_bytes),
    )


