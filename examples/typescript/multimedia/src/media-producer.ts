/**
 * media-producer - MCP Mesh Media Producer Agent
 *
 * Demonstrates generating media content and returning resource_links via the
 * mesh media API. Creates markdown reports, SVG charts, and PNG images using
 * only built-in Node.js APIs - no external imaging libraries required.
 *
 * Run with:
 *   npm run start:producer
 */

import { FastMCP, mesh, uploadMedia, mediaResult } from "@mcpmesh/sdk";
import { z } from "zod";
import zlib from "node:zlib";

const server = new FastMCP({ name: "Media Producer", version: "1.0.0" });

const agent = mesh(server, {
  name: "media-producer",
  httpPort: 9210,
  description: "Agent that generates media content and returns resource_links",
});

agent.addTool({
  name: "generate_report",
  capability: "report_generator",
  tags: ["media", "report"],
  description: "Generates a markdown report on a topic",
  parameters: z.object({
    topic: z.string().default("AI").describe("Topic for the report"),
  }),
  execute: async ({ topic }) => {
    const slug = topic.toLowerCase().replace(/ /g, "_");
    const content = `# Report: ${topic}

## Executive Summary

This is an auto-generated report on **${topic}** produced by the media-producer agent.

## Key Findings

1. ${topic} continues to evolve rapidly across multiple domains.
2. Adoption rates have increased significantly in the past year.
3. Integration with existing systems remains a priority.

## Recommendations

- Invest in foundational research for ${topic}.
- Develop clear governance frameworks.
- Monitor emerging trends and adjust strategy accordingly.

---
*Generated by media-producer agent*
`;
    const data = Buffer.from(content, "utf-8");
    const uri = await uploadMedia(data, `report_${slug}.md`, "text/markdown");
    return mediaResult(
      uri,
      `Report: ${topic}`,
      "text/markdown",
      `Markdown report on ${topic}`,
      data.length
    );
  },
});

agent.addTool({
  name: "generate_chart",
  capability: "chart_generator",
  tags: ["media", "chart"],
  description: "Generates an SVG bar chart from data",
  parameters: z.object({
    data: z
      .string()
      .default("Sales:40,Marketing:25,Engineering:60,Support:15")
      .describe("Data as 'Label:Value,...' pairs"),
  }),
  execute: async ({ data }) => {
    const pairs: [string, number][] = [];
    for (const item of data.split(",")) {
      const parts = item.trim().split(":");
      if (parts.length === 2) {
        const value = parseFloat(parts[1].trim());
        if (!isNaN(value)) {
          pairs.push([parts[0].trim(), value]);
        }
      }
    }

    if (pairs.length === 0) {
      pairs.push(["A", 30], ["B", 50], ["C", 20]);
    }

    const maxVal = Math.max(...pairs.map(([, v]) => v));
    const barWidth = 60;
    const gap = 20;
    const chartHeight = 200;
    const marginBottom = 40;
    const marginTop = 20;
    const marginLeft = 10;
    const totalWidth = marginLeft + pairs.length * (barWidth + gap) + gap;
    const totalHeight = chartHeight + marginBottom + marginTop;

    const colors = [
      "#4e79a7",
      "#f28e2b",
      "#e15759",
      "#76b7b2",
      "#59a14f",
      "#edc948",
      "#b07aa1",
      "#ff9da7",
    ];

    let barsSvg = "";
    for (let i = 0; i < pairs.length; i++) {
      const [label, value] = pairs[i];
      const barHeight = maxVal > 0 ? (value / maxVal) * chartHeight : 0;
      const x = marginLeft + gap + i * (barWidth + gap);
      const y = marginTop + chartHeight - barHeight;
      const color = colors[i % colors.length];
      barsSvg += `  <rect x="${x}" y="${y}" width="${barWidth}" height="${barHeight}" fill="${color}" rx="4"/>\n`;
      barsSvg += `  <text x="${x + barWidth / 2}" y="${marginTop + chartHeight + 16}" text-anchor="middle" font-size="12" font-family="sans-serif" fill="#333">${label}</text>\n`;
      barsSvg += `  <text x="${x + barWidth / 2}" y="${y - 5}" text-anchor="middle" font-size="11" font-family="sans-serif" fill="#555">${value}</text>\n`;
    }

    const svg = `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${totalWidth} ${totalHeight}" width="${totalWidth}" height="${totalHeight}">
  <rect width="100%" height="100%" fill="#fafafa" rx="8"/>
  <line x1="${marginLeft + gap}" y1="${marginTop + chartHeight}" x2="${totalWidth - gap}" y2="${marginTop + chartHeight}" stroke="#ccc" stroke-width="1"/>
${barsSvg}</svg>
`;
    const svgBytes = Buffer.from(svg, "utf-8");
    const uri = await uploadMedia(svgBytes, "chart.svg", "image/svg+xml");
    return mediaResult(
      uri,
      "Bar Chart",
      "image/svg+xml",
      `SVG bar chart with ${pairs.length} data points`,
      svgBytes.length
    );
  },
});

agent.addTool({
  name: "get_sample_image",
  capability: "image_generator",
  tags: ["media", "image"],
  description: "Returns a sample PNG image",
  parameters: z.object({}),
  execute: async () => {
    // Minimal 2x2 PNG with blue pixels, built from raw bytes.
    // PNG structure: signature + IHDR + IDAT + IEND
    const width = 2;
    const height = 2;

    // Each row: filter byte (0 = None) + RGB pixels
    const rawRows: number[] = [];
    for (let row = 0; row < height; row++) {
      rawRows.push(0x00); // filter byte
      for (let col = 0; col < width; col++) {
        rawRows.push(0x33, 0x66, 0xcc); // RGB blue-ish
      }
    }

    function makeChunk(chunkType: Buffer, data: Buffer): Buffer {
      const typeAndData = Buffer.concat([chunkType, data]);
      const crc = crc32(typeAndData);
      const lenBuf = Buffer.alloc(4);
      lenBuf.writeUInt32BE(data.length, 0);
      const crcBuf = Buffer.alloc(4);
      crcBuf.writeUInt32BE(crc >>> 0, 0);
      return Buffer.concat([lenBuf, typeAndData, crcBuf]);
    }

    function crc32(buf: Buffer): number {
      let crc = 0xffffffff;
      for (let i = 0; i < buf.length; i++) {
        crc ^= buf[i];
        for (let j = 0; j < 8; j++) {
          crc = crc & 1 ? (crc >>> 1) ^ 0xedb88320 : crc >>> 1;
        }
      }
      return (crc ^ 0xffffffff) >>> 0;
    }

    const signature = Buffer.from([0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a]);

    // IHDR: width(4) + height(4) + bitDepth(1) + colorType(1) + compression(1) + filter(1) + interlace(1)
    const ihdrData = Buffer.alloc(13);
    ihdrData.writeUInt32BE(width, 0);
    ihdrData.writeUInt32BE(height, 4);
    ihdrData[8] = 8;  // bit depth
    ihdrData[9] = 2;  // color type (RGB)
    ihdrData[10] = 0; // compression
    ihdrData[11] = 0; // filter
    ihdrData[12] = 0; // interlace
    const ihdr = makeChunk(Buffer.from("IHDR"), ihdrData);

    const compressed = zlib.deflateSync(Buffer.from(rawRows));
    const idat = makeChunk(Buffer.from("IDAT"), compressed);
    const iend = makeChunk(Buffer.from("IEND"), Buffer.alloc(0));

    const pngBytes = Buffer.concat([signature, ihdr, idat, iend]);

    const uri = await uploadMedia(pngBytes, "sample_image.png", "image/png");
    return mediaResult(
      uri,
      "Sample Image",
      "image/png",
      "A small 2x2 blue PNG image",
      pngBytes.length
    );
  },
});

